package Class2;

public class SwitchCase {
    public static void main(String[] arg) {
        int day = 2;
        switch (day) {
            case 1:
                System.out.println("monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("wednesday");
                break;

            default:
                System.out.println("thu-sat");
        }
    }

}
